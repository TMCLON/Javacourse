public class numbers {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        int add  = num1 + num2;
        int min = num1 - num2;
        int multi = num1 * num2;
        int div = num1 / num2;

        System.out.println(num1 + " add " +  num2 + " = " + add);
        System.out.println(num1 + " min " + num2 + " = " + min);
        System.out.println(num1 + " multi " + num2 + " = " + multi);
        System.out.println(num1 + " div " + num2 + " = " + div);

    }
}
